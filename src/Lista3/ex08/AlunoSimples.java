package Lista3.ex08;

public class AlunoSimples {
	private String nome;
	private double nota;
	
	public void setNota(double nota) {
		this.nota = nota;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public Double getNota() {
		return nota;
	}
	
	public String getNome() {
		return nome;
	}
}