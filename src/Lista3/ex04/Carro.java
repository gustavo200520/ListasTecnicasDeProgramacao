package Lista3.ex04;

public class Carro {
	private String modelo;
	private int ano;
	private double valor;
	
	
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public void setAno(int ano) {
		this.ano = ano;
	}
	public void setValor(double valor) {
		this.valor = valor;
	}
	
	public String getModelo() {
		return modelo;
	}
	public int getAno() {
		return ano;
	}
	public double getValor() {
		return valor;
	}
}
